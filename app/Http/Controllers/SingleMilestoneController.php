     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'milestone_name_edit' => 'required',
            'deposit_amount_edit' => 'required',
            'end_date_edit' => 'required',
        ]);

        $milestone = ContractMilestone::with('contract')->find($id);
        if($milestone->contract->created_by_user != auth()->id()){
            abort(403);
        }

        $milestone->name = $request->milestone_name_edit;
        $milestone->deposit_amount = $request->deposit_amount_edit;
        $milestone->end_date = $request->end_date_edit;
        $milestone->save();
    }

